<?php

/**
 * @param $name
 * @return string|void
 */
function post($name)
{
    if (isset($_POST[$name])) {
        return htmlspecialchars(trim($_POST[$name]));
    }
}

/**
 * @param $name
 * @return string|void
 */
function get($name)
{
    if (isset($_GET[$name])) {
        return htmlspecialchars(trim($_GET[$name]));
    }
}

/**
 * @param $name
 * @return mixed|void
 */
function session($name)
{
    if (isset($_SESSION[$name])) {
        return $_SESSION[$name];
    }
}

/**
 * @param $page
 * @param string $project_name
 * @return string
 */
function Title($page = null, string $project_name = PROJECT_NAME): string
{
    if (!$page) {
        return $project_name;
    } else {
        return "$page - $project_name";
    }
}

/**
 * @param string $url
 * @return void
 */
function site_url(string $url = REDIRECT_HOMEPAGE) {
    header('Location:' . $url);
    exit();
}

/**
 * @return mixed
 */
function getData(): mixed
{
    return json_decode(file_get_contents('php://input')); // Post data
}

/**
 * @param $value
 * @return string
 */
function input($value): string
{
    return htmlspecialchars(trim($value));
}

/**
 * @param string $message
 * @param string $targetId
 * @param int $status
 * @return false|string
 */
function message(string $message, string $targetId, int $status = 1): bool|string
{
    return json_encode([
        'message' => $message,
        'target' => $targetId,
        'status' => $status
    ]);
}

/**
 * @return void
 */
function LoginSession(): void
{
    if (!session('login')) {
        site_url();
    }
}

/**
 * @return string
 */
function generateRandomString(): string
{
    $length = 8;
    $characters = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNIOQRSTUVWXYZ";
    $string = "";

    for ($p = 0; $p < $length; $p++) {
        @$string .= $characters[mt_rand(0, strlen($characters))];
    }
    return $string;
}

/**
 * @param $data
 * @return void
 */
function jsonOutput($data): void
{
    header('Content-Type: application/json; charset=utf-8');
    echo json_encode($data);
    exit();
}

